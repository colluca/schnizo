def gen_trace_line(line, mc_exec,
                   lsu_pipelines, fpu_pipelines,
                   all_tracks, inflight_tracks, perf_events,
                   perf_metrics, producers, permissive) -> tuple[str, int, int]:
    sim_time, cycle, priv_lvl, loop_state, extras = parse_event(line)

    # Update stateful structures
    # We need to know
    # - In flight instructions to measure the latency of loads/stores
    # - instruction to producer mappings after LCP2 (for all instructions we must know the producer) -> ALU0.0 : addi t0, xcvxv
    #     This way we can print the nmemonics also during LEP
    # - the format of the currently written back float value

    # Performance data
    # - start / stop between mcycle CSR reads
    # - the total number of
    #    - instruction issues
    #    - stores / loads (separate for int and fp)
    #    - fpu instruction issues
    # - Store / Load latency of int and fp

    # FREP visualization
    # Each FREP loop generates a TraceEvent json file
    # We need to track all instructions.

    header = f"{sim_time:>8}  {cycle:>6}  {priv_lvl} {loop_state:<4} "  # trailing space
    trace_body = ""
    if (extras['event'] == EVENT_DISPATCH):
        handle_dispatch_event(sim_time, cycle, priv_lvl, loop_state, extras,
                              lsu_pipelines, fpu_pipelines,
                              perf_metrics)
        trace_body = gen_dispatch_trace(loop_state, extras, producers, mc_exec)
        gen_dispatch_perfetto(sim_time, cycle, priv_lvl, loop_state, extras,
                              all_tracks, inflight_tracks, perf_events, producers, mc_exec)
    elif (extras['event'] == EVENT_WRITEBACK):
        # Nothing statefull to handle - TODO: extract somehow the float format..
        trace_body = gen_writeback_trace(extras)
    elif (extras['event'] == EVENT_RESREQ):
        # Nothing statefull to handle
        trace_body = gen_resreq_trace(extras)
    elif (extras['event'] == EVENT_RESCAP):
        trace_body = gen_rescap_trace(extras)
        gen_rescap_perfetto(sim_time, cycle, priv_lvl, loop_state, extras,
                            all_tracks, inflight_tracks, perf_events)
    elif (extras['event'] == EVENT_RETIREMENT):
        handle_retirement_event(cycle, priv_lvl, loop_state, extras,
                                lsu_pipelines, fpu_pipelines, perf_metrics, permissive)
        gen_retirement_perfetto(sim_time, cycle, priv_lvl, loop_state, extras,
                                all_tracks, inflight_tracks, perf_events)
    elif (extras['event'] == EVENT_INTERNAL_SPATZ_ISSUE):
        gen_int_spatz_issue_perfetto(cycle, extras,
                                     all_tracks, inflight_tracks, perf_events)
    elif (extras['event'] == EVENT_INTERNAL_SPATZ_RETIREMENT):
        gen_int_spatz_retirement_perfetto(cycle, extras,
                                         all_tracks, inflight_tracks, perf_events)
    else:
        raise ValueError(f"Not a valid event type: {extras['event']}\n")

    if ((not extras['stall']) and extras['exception']):
        trace_body += " EXCEPTION"

    if (trace_body == ""):
        trace_line = ""
    else:
        trace_line = header + trace_body + "\n"

    return trace_line, sim_time, cycle


def safe_div(dividend, divisor, zero_div=0):
    return dividend / divisor if divisor else zero_div


def eval_perf_metrics(perf_metrics: list):
    for seg in perf_metrics:
        end = seg['end']
        # TODO: Add latency of FPU because at the end a FPU instruction could still be in flight.
        cycles = end - seg['start'] + 1  # +1 includes the csr read ending the segment
        seg.update({
            'schnizo_fpu_occupancy':
                safe_div(seg['schnizo_fpu_issues'], cycles),
            'schnizo_fpu_avg_latency':
                safe_div(seg['schnizo_fpu_latency'], seg['schnizo_fpu_issues']),
            'schnizo_avg_load_latency':
                safe_div(seg['schnizo_load_latency'], seg['schnizo_loads']),
            'schnizo_avg_gpr_load_latency':
                safe_div(seg['schnizo_gpr_load_latency'], seg['schnizo_gpr_loads']),
            'schnizo_avg_fpr_load_latency':
                safe_div(seg['schnizo_fpr_load_latency'], seg['schnizo_fpr_loads']),
            'cycles':
                cycles,
            'total_ipc':
                safe_div(seg['schnizo_issues'], cycles),
        })
    return 0


def fmt_perf_metrics(perf_metrics: list, idx: int, omit_keys: bool = True):
    ret = [
        'Performance metrics for section {} @ ({}, {}):'.format(
            idx, perf_metrics[idx]['start'], perf_metrics[idx]['end'])
    ]
    for key, val in perf_metrics[idx].items():
        if omit_keys and key in PERF_EVAL_KEYS_OMIT:
            continue
        if val is None:
            val_str = str(None)
        elif isinstance(val, float):
            val_str = flt_fmt(val, 4)
        else:
            as_hex = False if key in PERF_EVAL_KEYS_DECIMAL else None
            val_str = int_lit(val, as_hex=as_hex)
        ret.append('{:<40}{:>10}'.format(key, val_str))
    return '\n'.join(ret)


