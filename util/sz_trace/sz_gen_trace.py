# -------------------- Float helpers  --------------------
def flt_decode(val: int, fmt: int) -> float:
    """Interprets the binary encoding of an integer as a FP value.

    Args:
        val: The integer encoding of the FP variable to decode.
        fmt: The floating point number format, as an index into the
            `FLOAT_FMTS` array.
    Returns:
        The floating point value represented by the input integer.
    """
    # get format and bit vector
    w_exp, w_mnt = FLOAT_FMTS[fmt]
    width = 1 + w_exp + w_mnt
    bitstr = '{:064b}'.format(val)[-width:]
    # print(bitstr)
    # Read bit vector slices
    sgn = -1.0 if bitstr[0] == '1' else 1.0
    mnt = int(bitstr[w_exp + 1:], 2)
    exp_unb = int(bitstr[1:w_exp + 1], 2)
    # derive base and exponent
    bse = int('1' + bitstr[w_exp + 1:], 2) / (2**w_mnt)
    exp_bias = -(2**(w_exp - 1) - 1)
    exp = exp_unb + exp_bias
    # case analysis
    if exp_unb == 2**w_exp - 1:
        return sgn * float('inf' if mnt == 0 else 'nan')
    elif exp_unb == 0 and mnt == 0:
        return sgn * 0.0
    elif exp_unb == 0:
        return float(sgn * mnt / (2**w_mnt) * (2**(exp_bias + 1)))
    else:
        return float(sgn * bse * (2**exp))


def flt_fmt(flt: float, width: int = 6) -> str:
    """Formats a floating-point number rounding to a certain decimal precision.

    Args:
        flt: The floating-point number to format.
        width: The number of significant decimal digits to round to.
    Returns:
        The formatted floating-point number as a string.
    """
    fmt = '{:.' + str(width) + '}'
    return fmt.format(flt)


# -------------------- Literal formatting  --------------------
def int_lit(num: int, size: int = 2, as_hex: Optional[bool] = None, prefix: Optional[bool] = True) -> str:
    width = (8 * int(2**size))
    size_mask = (0x1 << width) - 1
    num = num & size_mask  # num is unsigned
    num_signed = c_int32(c_uint32(num).value).value
    if as_hex is True or abs(num_signed) > MAX_SIGNED_INT_LIT and as_hex is not False:
        if prefix is True:
            return '0x{0:0{1}x}'.format(num, width // 4)
        else:
            return '{0:0{1}x}'.format(num, width // 4)
    else:
        return str(num_signed)


def flt_lit(num: int, fmt: int, width: int = 6, vlen: int = 1) -> str:
    """Formats an integer encoding into a floating-point literal.

    Args:
        num: The integer encoding of the floating-point number(s).
        fmt: The floating point number format, as an index into the
            `FLOAT_FMTS` array.
        width: The bitwidth of the floating-point type.
        vlen: The number of floating-point numbers packed in the encoding,
            >1 for SIMD vectors.
    """
    # Divide the binary encoding into individual encodings for each number in the SIMD vector.
    bitwidth = 1 + FLOAT_FMTS[fmt][0] + FLOAT_FMTS[fmt][1]
    vec = [num >> (bitwidth * i) & (2**bitwidth - 1) for i in reversed(range(vlen))]
    # Format each individual float encoding to a string.
    floats = [flt_fmt(flt_decode(val, fmt), width) for val in vec]
    # Represent the encodings as a vector if SIMD.
    if len(floats) > 1:
        return '[{}]'.format(', '.join(floats))
    else:
        return floats[0]


@lru_cache
def disasm_inst(hex_inst, mc_exec='llvm-mc', mc_flags='-disassemble -mcpu=snitch -mattr=+v'):
    """Disassemble a single RISC-V instruction using llvm-mc."""
    # Reverse the endianness of the hex instruction
    inst_fmt = ' '.join(f'0x{byte:02x}' for byte in bytes.fromhex(hex_inst)[::-1])

    # Use llvm-mc to disassemble the binary instruction
    result = subprocess.run(
        [mc_exec] + mc_flags.split(),
        input=inst_fmt,
        capture_output=True,
        text=True,
        check=True,
    )

    # Extract disassembled instruction from llvm-mc output
    return result.stdout.splitlines()[-1].strip().replace('\t', ' ')


def parse_cycle(line: str) -> int:
    match = re.match(DASM_LINE_REGEX, line)
    if match:
        return match.group("cycle")
    else:
        raise ValueError(f"Not a valid trace line:\n{line}")


# Takes a DASM event line and parses it. Returns a tuple of::
# time: The current sim time
# cycle: The current cycle
# priv_lvl: The CPU privilege level
# loop_state: FREP loop state
# extras: Dict with extra data depending on event
def parse_event(line: str) -> tuple[int, int, str, str, dict]:
    match = re.match(DASM_LINE_REGEX, line)
    if match:
        # Extract the named groups
        sim_time = int(match.group("sim_time"))
        cycle = int(match.group("cycle"))
        priv_lvl = match.group("priv_lvl")
        loop_state = match.group("loop_state")
        extra_str = match.group("extra_str").strip("{}")

        # Convert the extra string to a dict
        extras = ast.literal_eval(f"{{{extra_str}}}")
        return sim_time, cycle, priv_lvl, loop_state, extras
    else:
        raise ValueError(f"Not a valid trace line:\n{line}")


# Parses the mnemomic, pc, rs2 and rd from dispatch extras.
# !! MUST BE a "Regular" dispatch event !! (LOOP_REGULAR, LOOP_HWLOOP, LOOP_LCP1, LOOP_LCP2)
def parse_mnemomic_from_dispatch(extras: dict, mc_exec: str) -> tuple[str, str, int, int]:
    mnemomic = disasm_inst(int_lit(extras['instr_data'], as_hex=True, prefix=False), mc_exec)
    pc_str = int_lit(extras['pc_q'], as_hex=True)
    rs2 = extras['rs2']
    rd = extras['rd']

    return pc_str, mnemomic, rs2, rd


# producers is a dict with key "producer" and a tuple containing
# pc_str, mnemomic, rs2, rd, iter_count
def gen_dispatch_trace(loop_state, extras, producers, mc_exec) -> str:
    if (loop_state in {LOOP_REGULAR, LOOP_HWLOOP, LOOP_LCP1, LOOP_LCP2}):
        pc_str, mnemomic, rs2, rd = parse_mnemomic_from_dispatch(extras, mc_exec)
        iter_count = 1

        # FREP producer mapping for LEP trace details
        # Reset mapping in Regular, HW loop & LCP1
        # (If nested: create new mapping when entering LCP1)
        # Build mapping in LCP2
        # Don't change mapping during LEP
        # (If nested: destroy mapping when leaving LEP)
        if (loop_state in {LOOP_REGULAR, LOOP_HWLOOP, LOOP_LCP1}):
            producers = {}
        elif (loop_state in {LOOP_LCP2}):
            pc_str, mnemomic, rs2, rd = parse_mnemomic_from_dispatch(extras, mc_exec)
            # start the iteration count after two iterations
            iter_count = 2
            producers[extras['disp_resp']] = (pc_str, mnemomic, rs2, rd, iter_count)

        fu_str = extras['disp_resp']
    elif loop_state in {LOOP_LEP}:
        fu_str = extras['producer']
        if (extras['producer'] in producers):
            pc_str, mnemomic, rs2, rd, iter_count = producers[extras['producer']]
            # incremetn the interation count for this producer
            iter_count += 1
            producers[extras['producer']] = (pc_str, mnemomic, rs2, rd, iter_count)
        else:
            mnemomic = "no lep data"
            pc_str = "0x????????"
            rs2 = 0
            rd = 0
            iter_count = 0
    else:
        raise ValueError(f"Not a valid loop state:{loop_state}\n")

    # Don't print the fu if in regular mode
    if (loop_state not in {LOOP_LCP1, LOOP_LCP2, LOOP_LEP}):
        fu_str = ""
    txt = f"{pc_str:<10} {mnemomic:<26}  {fu_str:<6} #; "

    # LSU
    is_lsu = False
    if ('fu_type' in extras):
        is_lsu = extras['fu_type'] in {FU_LSU}
    elif ('producer' in extras):
        is_lsu = extras['producer'].startswith(FU_LSU)

    comment = []
    # LSU
    if (is_lsu):
        if (extras['lsu_is_store']):
            register = REG_ABI_NAMES_F[rs2] if extras['lsu_is_float'] else REG_ABI_NAMES_I[rs2]
            fmt = LSU_SIZE_TO_FLOAT[extras['lsu_size']]
            value_str = flt_lit(extras['lsu_store_data'], fmt) if extras['lsu_is_float'] else int_lit(extras['lsu_store_data'])
            comment.append(f"{register} = {value_str} ~~> {LSU_SIZES[extras['lsu_size']]}[{int_lit(extras['lsu_addr'], as_hex=True)}]")
        elif (extras['lsu_is_load']):
            register = REG_ABI_NAMES_F[rd] if extras['lsu_is_float'] else REG_ABI_NAMES_I[rd]
            comment.append(f"{register} <~~ {LSU_SIZES[extras['lsu_size']]}[{int_lit(extras['lsu_addr'], as_hex=True)}]")
    # CSR
    if ('fu_type' in extras):
        if (extras['fu_type'] == FU_CSR):
            csr_name = CSR_NAMES[extras['csr_addr']] if extras['csr_addr'] in CSR_NAMES else f"csr@{int_lit(extras['csr_addr'], as_hex=True)}"
            comment.append(f"{csr_name} = {int_lit(extras['csr_write_data'])}")
    # Branch?
    if (extras.get('is_branch', False)):
        comment.append("taken" if extras['branch_taken'] else "not taken")
    # Jump?
    if not extras['stall'] and (extras.get('pc_d', 4) != (extras.get('pc_q', 0) + 4)):
        comment.append(f"goto {int_lit(extras['pc_d'], as_hex=True)}")
    # Instruction iter if in FREP
    if (('instr_iter' in extras) and loop_state in {LOOP_LCP1, LOOP_LCP2, LOOP_LEP}):
        comment.append(f"instr iter = {iter_count} ({extras['instr_iter']})")

    txt += ", ".join(comment)
    return txt


# flt_fmt is the float format if we have a writeback in this cycle.
# This values must come from the state handling logic as the DASM cannot provide it.
def gen_writeback_trace(extras):
    flt_fmt = 0  # TODO: somehow get the flt format?
    register = REG_ABI_NAMES_F[extras['rd']] if extras['rd_is_fp'] else REG_ABI_NAMES_I[extras['rd']]
    value_str = flt_lit(extras['result'], flt_fmt) if extras['rd_is_fp'] else int_lit(extras['result'])
    writeback = f"{register} = {value_str}"
    if (extras['rd'] == 0 and not extras['rd_is_fp']):
        # This is a writeback to GPR 0, which is fixed to all zeros. Schnizo uses dummy writebacks
        # to this GPR for instructions which don't write any registers.
        return ""
    return f"{'':<10} {'':<26}  {'':<6} #; {extras['origin']}: {writeback}"


def gen_resreq_trace(extras):
    return f"{'':<10} {'':<26}  {'':<6} #; RESREQ to {extras['producer']} from {extras['consumer']}, result iter = {extras['requested_iter']}"


def gen_rescap_trace(extras):
    return f"{'':<10} {'':<26}  {'':<6} #; RESCAP {extras['producer']} <-- {int_lit(extras['result'])} {'(float)' if extras['rd_is_fp'] else ''}, result iter = {extras['result_iter']}"


def handle_dispatch_event(sim_time, cycle, priv_lvl, loop_state, extras,
                          lsu_pipelines, fpu_pipelines, perf_metrics):
    if extras['stall'] and not (loop_state in {LOOP_LEP}):
        return

    perf_metrics[-1]['schnizo_issues'] += 1

    # CSR
    if ('fu_type' in extras):
        if (extras['fu_type'] == FU_CSR):
            csr_addr = extras['csr_addr']
            csr_name = CSR_NAMES[csr_addr] if csr_addr in CSR_NAMES else ""
            if csr_name == CSR_MCYCLE:
                # Schnizo only supports reading the mcycle CSR despite in the RISCV standard it is MRW.
                cycles_passed = extras['csr_read_data']
                perf_metrics[-1]['tend'] = sim_time // 1000
                perf_metrics[-1]['end'] = cycles_passed  # does not include the current cycle
                perf_metrics.append(defaultdict(int))
                perf_metrics[-1]['tstart'] = sim_time // 1000
                perf_metrics[-1]['start'] = cycles_passed + 2  # next section starts from next cycle onwards

    # FPU
    is_fpu = False
    if ('fu_type' in extras):
        is_fpu = extras['fu_type'] in {FU_FPU}
        fpu_id = extras['disp_resp'].split('.')[0]
    elif ('producer' in extras):
        is_fpu = extras['producer'].startswith(FU_FPU)
        fpu_id = extras['producer'].split('.')[0]

    if (is_fpu):
        perf_metrics[-1]['schnizo_fpu_issues'] += 1
        fpu_pipelines[fpu_id].appendleft((cycle, LSU_SIZE_TO_FLOAT[extras['fpu_dst_fmt']]))

    # LSU
    is_lsu = False
    lsu_id = ""
    if ('fu_type' in extras):
        is_lsu = extras['disp_resp'].startswith(FU_LSU)
        # keep only the first characters and all number until the first .
        lsu_id = extras['disp_resp'].split('.')[0]
    elif ('producer' in extras):
        is_lsu = extras['producer'].startswith(FU_LSU)
        lsu_id = extras['producer'].split('.')[0]

    if (is_lsu):
        if (extras['lsu_is_load']):
            perf_metrics[-1]['schnizo_loads'] += 1
            lsu_pipelines[lsu_id].appendleft((cycle, extras['lsu_is_float']))
            if (extras['lsu_is_float']):  # save also float format for writeback float literal
                perf_metrics[-1]['schnizo_fpr_loads'] += 1
            else:
                perf_metrics[-1]['schnizo_gpr_loads'] += 1
        elif (extras['lsu_is_store']):
            perf_metrics[-1]['schnizo_stores'] += 1
            if (extras['lsu_is_float']):
                perf_metrics[-1]['schnizo_fpr_stores'] += 1
            else:
                perf_metrics[-1]['schnizo_gpr_stores'] += 1


    is_spatz = False
    if ('fu_type' in extras):
        is_spatz = extras['fu_type'] in {FU_SPATZ}
    elif ('producer' in extras):
        is_spatz = extras['producer'].startswith(FU_SPATZ)
    
    if (is_spatz):
        perf_metrics[-1]['spatz_issues'] += 1

    return 0


